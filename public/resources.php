				<h2>Resources</h2>
				<p>Sometimes you need more than a song. 
				If you're in a desperate situation, please get help. Seriously.</p>
				<p>The hardest thing to remember when you're feeling like you can't go on is that your feeling is temporary. 
				I know it doesn't feel like it, but it is. Before you take any action, <a href="http://www.metanoia.org/suicide/">read this</a>!</p>
